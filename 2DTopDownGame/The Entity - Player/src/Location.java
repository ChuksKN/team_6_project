
public class Location {
	private int xcoord;
	private int ycoord;
	
	public Location(int xcoord, int ycoord) {
		this.xcoord = xcoord;
		this.ycoord = ycoord;
		
	}
	public void setxcoord(int xcoord) {
		this.xcoord = xcoord;
	}
	public int getxcoord() {
		return xcoord;
	}
	public void setycoord(int ycoord) {
		this.ycoord = ycoord;
	}
	public int getycoord() {
		return ycoord;
	}

}
